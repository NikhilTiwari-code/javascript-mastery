//switch case

const day = "wednesday";
switch (day) {
            case "sunday":
                console.log("sunday");
        
                break;
        
    
            case" monday":
                console.log("monday");
        
                break;
            case "tuesday":
                console.log("tuesday");
        
                break;
            case "wednesday":
                console.log("wednesday");
        
                break;
            
                default:
                    console.log("Invalid day");
                    break;
}